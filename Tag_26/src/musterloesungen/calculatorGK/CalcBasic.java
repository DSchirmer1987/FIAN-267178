package musterloesungen.calculatorGK;

public class CalcBasic {

	// exercise 2)
	public static String sum(int first, int second) {
		return Integer.toString(first + second);
	}
	
	public static String prod(int first, int second) {
		return Integer.toString(first * second);
	}
	
	public static String subt(int first, int second) {
		return Integer.toString(first - second);
	}
	
	public static String div(int first, int second) {
		return Integer.toString(first / second);
	}
	
	// exercise 3)
	public static String sum(double first, double second) {
		return Double.toString(first + second);
	}
	
	public static String prod(double first, double second) {
		return Double.toString(first * second);
	}
	
	public static String subt(double first, double second) {
		return Double.toString(first - second);
	}
	
	public static String div(double first, double second) {
		return Double.toString(first / second);
	}

}
